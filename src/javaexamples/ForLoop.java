package javaexamples;

public class ForLoop {

	public static void main(String[] args) {
for(int i=1; i<=5; i++) {
	System.out.println("narendra::"+i);
}
for(int i=1; i<=5; i+=2) {
	System.out.println("narendra::"+i);
}
for(int i=5; i>=1; i--) {
	System.out.println("narendra::"+i);
}
for(int i=5; i>=1; i-=2) {
	System.out.println("narendra::"+i);
}

	}

}
