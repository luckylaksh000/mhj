package javaexamples;

public class For_loop {

	public static void main(String[] args) {
		//for (initialization; cond; inc/dec){ for ex:
		for (int i=1; i<10; i++) {
			System.out.println(i);
		}
		for (int i=1; i<10; i+=2) {
			System.out.println(i);
		}
		for (int i=5; i>=1; i--) {
			System.out.println(i);
		}
		for (int i=5; i>=1; i-=2) {
			System.out.println(i);
		}


	}
}
