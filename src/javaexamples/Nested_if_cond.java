package javaexamples;

public class Nested_if_cond {

	public static void main(String[] args) {

		int a=20;
		int b=50;
		if (a==10)//1st cond
		{
			if (b==40)// 2nd cond 
			{
				System.out.println("a value is 20 & b value is 40 ");
			}
			else {
				System.out.println("else stmnt of 2nd cond");
			}
		}
		else {
			System.out.println("else stmnt of 1st cond");

		}
	}






}


