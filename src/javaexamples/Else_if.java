package javaexamples;

public class Else_if {

	public static void main(String[] args) {
		int a=40;
		if(a==20)
		{
			System.out.println("print 1st Statement");
		}
		else if(a>40)
		{
			System.out.println("print 2nd Statement");
		}
		else if(a>50)
		{
			System.out.println("print 3rd Statement");
		}
		else 
		{
			System.out.println("print 4th Statement");
		}
System.out.println("No one");
	}

}
